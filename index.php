                  <h4>What is Pacemaker?</h4>
                  <p>Pacemaker is a small device which is implanted via surgical procedure near heart to control the heartbeat. And it is placed to prevent the heart from beating too slowly. <span id="dots">...</span><span id="more">A pacemaker is also called a cardiac pacing device.<br> 
                    A pacemaker is also called a cardiac pacing device.<br><strong>Types:-</strong> <br>Depending on your condition, you might have one of the following types of pacemakers.
                    <br>
                    Single chamber pacemaker. This type usually carries electrical impulses to the right ventricle of your heart. <br>
                    Dual chamber pacemaker. This type carries electrical impulses to the right ventricle and the right atrium of your heart to help control the timing of contractions between the two chambers.
                  <br> Biventricular pacemaker. Biventricular pacing, also called cardiac resynchronization therapy, is for people who have heart failure and heartbeat problems. This type of pacemaker stimulates both of the lower heart chambers (the right and left ventricles) to make the heart beat more efficiently. <br>
                <strong>Why it's done? </strong> <br>
                A pacemaker is implanted to help control your heartbeat. Your doctor may recommend a temporary pacemaker when you have a slow heartbeat (bradycardia) after a heart attack, surgery or medication overdose but your heartbeat is otherwise expected to recover. A pacemaker may be implanted permanently to correct a chronic slow or irregular heartbeat or to help treat heart failure. <br>
                <strong>How your heart beats?</strong>
                The heart is a muscular, fist-sized pump with four chambers, two on the left side and two on the right. The upper chambers (right and left atria) and the lower chambers (right and left ventricles) work with your heart's electrical system to keep your heart beating at an appropriate rate — usually 60 to 100 beats a minute for adults at rest. <br>
                Your heart's electrical system controls your heartbeat, beginning in a group of cells at the top of the heart (sinus node) and spreading to the bottom, causing it to contract and pump blood. Aging, heart muscle damage from a heart attack, some medications and certain genetic conditions can cause an irregular heart rhythm. <br>
                <strong>What a pacemaker does?</strong> <br>
                What a pacemaker does?
              Pacemakers work only when needed. If your heartbeat is too slow (bradycardia), the pacemaker sends electrical signals to your heart to correct the beat. <br>
            Some newer pacemakers also have sensors that detect body motion or breathing rate and signal the devices to increase heart rate during exercise, as needed. <br>
